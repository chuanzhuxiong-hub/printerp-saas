import { PrismaClient } from "@prisma/client";

const db = new PrismaClient();
const baseUrl = process.env.APP_URL ?? "http://localhost:3000";
const startedAt = new Date();

async function login() {
  const response = await fetch(`${baseUrl}/api/auth/login`, {
    method: "POST",
    body: new URLSearchParams({ email: "owner@demo.printerp.local", password: "PrintERP123!" }),
    redirect: "manual"
  });
  if (response.status !== 303) throw new Error(`登录失败 (${response.status})`);
  return response.headers.get("set-cookie")?.split(";")[0] ?? "";
}

async function upload(cookie, targetType, targetId) {
  const form = new FormData();
  form.set("targetType", targetType);
  form.set("targetId", targetId);
  form.set("file", new File([
    "; generated by PrusaSlicer 2.8\n; filament used [g] = 33.333\n; estimated printing time (normal mode) = 2h 30m\n"
  ], "integration.gcode", { type: "text/plain" }));
  const response = await fetch(`${baseUrl}/api/gcode`, { method: "POST", headers: { cookie }, body: form, redirect: "manual" });
  if (response.status !== 303) throw new Error(`G-code 更新失败 (${response.status}): ${await response.text()}`);
}

const tenant = await db.tenant.findFirstOrThrow();
const user = await db.user.findFirstOrThrow({ where: { email: "owner@demo.printerp.local" } });
const bom = await db.productBom.findFirstOrThrow({ where: { tenantId: tenant.id, deletedAt: null } });
const originalBom = {
  theoreticalGrams: bom.theoreticalGrams,
  estimatedPrintHours: bom.estimatedPrintHours,
  estimatedProductCost: bom.estimatedProductCost,
  updatedBy: bom.updatedBy
};
const production = await db.productionOrder.create({
  data: {
    tenantId: tenant.id,
    orderNo: `GCODE-${Date.now()}`,
    skuId: bom.skuId,
    plannedQuantity: 1,
    createdBy: user.id,
    items: { create: [{ tenantId: tenant.id, skuId: bom.skuId, plannedQuantity: 1 }] }
  }
});

try {
  const cookie = await login();
  await upload(cookie, "BOM", bom.id);
  await upload(cookie, "PRODUCTION", production.id);
  const [updatedBom, updatedProduction] = await Promise.all([
    db.productBom.findUniqueOrThrow({ where: { id: bom.id } }),
    db.productionOrder.findUniqueOrThrow({ where: { id: production.id } })
  ]);
  if (!updatedBom.theoreticalGrams.equals("33.333") || !updatedBom.estimatedPrintHours.equals("2.50")) throw new Error("BOM G-code 数据未正确更新");
  if (!updatedProduction.actualMaterialGrams.equals("33.333") || !updatedProduction.actualPrintHours.equals("2.50")) throw new Error("生产任务 G-code 数据未正确预填");
  console.log("G-code integration passed: BOM update and production prefill");
} finally {
  await db.productBom.update({ where: { id: bom.id }, data: originalBom });
  await db.auditLog.deleteMany({ where: { tenantId: tenant.id, action: "gcode.analyzed", createdAt: { gte: startedAt } } });
  await db.productionOrderItem.deleteMany({ where: { productionOrderId: production.id } });
  await db.productionOrder.delete({ where: { id: production.id } });
  await db.$disconnect();
}
